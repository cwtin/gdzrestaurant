const categoryNav = document.getElementById("categoryNav");
const menuWrapper = document.getElementById("menu");
const backToTop = document.getElementById("backToTop");

let activeCategoryId = menuData[0].id;

function createCategoryNav() {
  categoryNav.innerHTML = "";

  menuData.forEach(category => {
    const button = document.createElement("button");
    button.type = "button";
    button.className = "category-link";
    button.dataset.id = category.id;
    button.textContent = category.title;

    if (category.id === activeCategoryId) {
      button.classList.add("active");
    }

    button.addEventListener("click", () => {
      showCategory(category.id);
    });

    categoryNav.appendChild(button);
  });
}

function showCategory(categoryId) {
  const category = menuData.find(cat => cat.id === categoryId);
  if (!category) return;

  activeCategoryId = categoryId;
  updateActiveButton();

  const contentHtml = category.groups
    ? category.groups.map(group => `
        <div class="menu-group">
          <div class="group-title">
            <span></span>
            <h3>${group.title}</h3>
            <span></span>
          </div>

          ${group.items.map(item => `
            <div class="product-row">
              <div class="product-left">
                <h4>${item.name}</h4>
                <p>${item.description}</p>
                ${item.calories ? `<span>${item.calories}</span>` : ""}
              </div>

              <div class="product-price">
                ${item.price}
              </div>
            </div>
          `).join("")}
        </div>
      `).join("")
    : category.items.map(item => `
        <div class="product-row">
          <div class="product-left">
            <h3>${item.name}</h3>
            <p>${item.description}</p>
            ${item.calories ? `<span>${item.calories}</span>` : ""}
          </div>

          <div class="product-price">
            ${item.price}
          </div>
        </div>
      `).join("");

  menuWrapper.innerHTML = `
    <section class="category-page">

      <div class="category-hero" style="background-image: url('${category.image}')">
        <div class="category-hero-overlay"></div>
        <h2>${category.title}</h2>
      </div>

      <div class="product-list">
        ${contentHtml}
      </div>

    </section>
  `;

  const menuTop = menuWrapper.offsetTop - categoryNav.offsetHeight - 80;

  window.scrollTo({
    top: Math.max(menuTop, 0),
    behavior: "smooth"
  });

  scrollActiveButtonToCenter();
}

function updateActiveButton() {
  document.querySelectorAll(".category-link").forEach(button => {
    button.classList.toggle("active", button.dataset.id === activeCategoryId);
  });
}

function scrollActiveButtonToCenter() {
  const activeButton = document.querySelector(".category-link.active");
  if (!activeButton) return;

  activeButton.scrollIntoView({
    behavior: "smooth",
    inline: "center",
    block: "nearest"
  });
}

function handleBackToTop() {
  if (!backToTop) return;
  backToTop.classList.toggle("show", window.scrollY > 500);
}

if (backToTop) {
  backToTop.addEventListener("click", () => {
    window.scrollTo({
      top: 0,
      behavior: "smooth"
    });
  });
}

window.addEventListener("scroll", handleBackToTop);

document.addEventListener("DOMContentLoaded", () => {
  createCategoryNav();
  showCategory(activeCategoryId);
});

